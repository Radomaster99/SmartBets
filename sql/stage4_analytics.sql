    league_id bigint NOT NULL REFERENCES leagues(id) ON DELETE CASCADE,
    season integer NOT NULL,
    rank integer NOT NULL,
    api_player_id bigint NOT NULL,
    player_name character varying(200) NOT NULL,
    player_photo_url character varying(500) NULL,
    team_id bigint NULL,
    team_api_id bigint NULL,
    team_name character varying(200) NOT NULL,
    team_logo_url character varying(500) NULL,
    appearances integer NULL,
    minutes integer NULL,
    position character varying(50) NULL,
    rating numeric(6,2) NULL,
    goals integer NULL,
    assists integer NULL,
    passes_key integer NULL,
    chances_created integer NULL,
    synced_at_utc timestamp with time zone NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS ix_league_top_assists_league_id_season_rank
    ON league_top_assists (league_id, season, rank);

CREATE INDEX IF NOT EXISTS ix_league_top_assists_league_id_season_api_player_id
    ON league_top_assists (league_id, season, api_player_id);

CREATE TABLE IF NOT EXISTS league_top_cards (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    league_id bigint NOT NULL REFERENCES leagues(id) ON DELETE CASCADE,
    season integer NOT NULL,
    combined_rank integer NOT NULL,
    yellow_rank integer NULL,
    red_rank integer NULL,
    api_player_id bigint NOT NULL,
    player_name character varying(200) NOT NULL,
    player_photo_url character varying(500) NULL,
    team_id bigint NULL,
    team_api_id bigint NULL,
    team_name character varying(200) NOT NULL,
    team_logo_url character varying(500) NULL,
    appearances integer NULL,
    minutes integer NULL,
    position character varying(50) NULL,
    rating numeric(6,2) NULL,
    yellow_cards integer NOT NULL DEFAULT 0,
    red_cards integer NOT NULL DEFAULT 0,
    synced_at_utc timestamp with time zone NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS ix_league_top_cards_league_id_season_combined_rank
    ON league_top_cards (league_id, season, combined_rank);

CREATE INDEX IF NOT EXISTS ix_league_top_cards_league_id_season_api_player_id
    ON league_top_cards (league_id, season, api_player_id);
